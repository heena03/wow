<script>
document.addEventListener("DOMContentLoaded", function () {
    var text = "Welcome to ~hk world";
    var animatedText = document.getElementById("welcomeText");
    var audioMessage = document.getElementById("audioMessage");
    var audioPlayed = false;

    function revealText(index) {
        animatedText.textContent = text.substring(0, index);
        if (index <= text.length) {
            // Play audio when revealing each letter, but only if not already played
            if (!audioPlayed) {
                audioMessage.play().then(function () {
                    audioPlayed = true;
                }).catch(function (error) {
                    console.error("Error playing audio:", error);
                });
            }

            setTimeout(function () {
                revealText(index + 1);
            }, 100); // Adjust the timeout for the speed of the animation
        } else {
            // Animation complete, redirect to index.html
            window.location.href = "index.html";
        }
    }

    // Start the animation
    revealText(0);
});
</script>

